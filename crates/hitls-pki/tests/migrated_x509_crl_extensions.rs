/// Mirrors C `SDV_X509_CRL_PARSE_EXTENSIONS_FUNC_TC001` baseline row:
/// `test_crl.pem` is the plain extension-carrying CRL the
/// `extension_crl/` fixtures derive from.
#[test]
fn crl_parse_extension_baseline_test_crl() {
    let crl = load_crl("cert/test_for_crl/extension_crl/test_crl.pem");
    assert_eq!(crl.version, 2);
    // The baseline carries a CRLNumber; pin its presence.
    assert!(
        crl.crl_number().is_some(),
        "baseline test_crl.pem must carry a CRLNumber extension"
    );
}

