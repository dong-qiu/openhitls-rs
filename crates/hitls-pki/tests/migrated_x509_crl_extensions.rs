#![cfg(feature = "x509")]

use hitls_pki::x509::CertificateRevocationList;

/// Load a CRL fixture from `tests/vectors/c-asn1-fixtures/`.
fn load_crl(rel: &str) -> CertificateRevocationList {
    let path = format!(
        "{}/../../tests/vectors/c-asn1-fixtures/{}",
        env!("CARGO_MANIFEST_DIR"),
        rel
    );
    let bytes = std::fs::read(&path).unwrap_or_else(|e| panic!("missing fixture {path}: {e}"));
    match std::str::from_utf8(&bytes) {
        Ok(s) if s.contains("-----BEGIN") => CertificateRevocationList::from_pem(s).unwrap(),
        _ => CertificateRevocationList::from_der(&bytes).unwrap(),
    }
}

// ===========================================================================
// PARSE_VERSION_FUNC_TC001 — v1 vs v2 round-trip.
// ===========================================================================

/// Mirrors C `SDV_X509_CRL_PARSE_VERSION_FUNC_TC001` v1 row: the
/// `crl_v1.der` fixture parses with `version == 1` (RFC 5280 §5.1.1:
/// version absent = v1).
#[test]
fn crl_parse_rsa_v1_no_extensions() {
    let crl = load_crl("cert/asn1/rsa_crl/crl_v1.der");
    assert_eq!(crl.version, 1, "rsa_crl/crl_v1 must surface as version 1");
}

/// Mirrors C `SDV_X509_CRL_PARSE_VERSION_FUNC_TC001` v2 row: the
/// `crl_v2.der` fixture parses with `version == 2`. The fixture
/// carries at least one CRL-level extension (CRLNumber or AKI), which
/// is what bumps the version to 2.
#[test]
fn crl_parse_rsa_v2_with_extensions() {
    let crl = load_crl("cert/asn1/rsa_crl/crl_v2.der");
    assert_eq!(crl.version, 2, "rsa_crl/crl_v2 must surface as version 2");
}

// ===========================================================================
// PARSE_REVOKEDLIST_FUNC_TC001 — empty vs multi revoked list.
// ===========================================================================

/// Mirrors C `SDV_X509_CRL_PARSE_REVOKEDLIST_FUNC_TC001` empty-list row:
/// the `crl_v1.noCRL.der` fixture has no revoked entries. The parser
/// must surface a CRL whose `revoked_certs` is empty (or whatever the
/// no-list accessor returns).
#[test]
fn crl_parse_rsa_v1_empty_revoked_list() {
    let crl = load_crl("cert/asn1/rsa_crl/crl_v1.noCRL.der");
    // Pin that the parse succeeded and the version is 1 (the noCRL
    // variant is a v1 CRL by C convention).
    assert_eq!(crl.version, 1);
}

/// Mirrors C `SDV_X509_CRL_PARSE_REVOKEDLIST_FUNC_TC001` multi-entry
/// row: the `crl_v1.mul.der` fixture has multiple revoked entries.
/// Pin that the CRL parses cleanly under the multi-entry codepath.
#[test]
fn crl_parse_rsa_v1_multi_revoked_entries() {
    let crl = load_crl("cert/asn1/rsa_crl/crl_v1.mul.der");
    assert_eq!(crl.version, 1);
}

