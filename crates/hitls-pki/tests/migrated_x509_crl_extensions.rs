/// Mirrors C `SDV_X509_CRL_PARSE_SIGNALG_FUNC_TC001` ECDSA row:
/// the `ecdsa_crl/crl_v2.der` fixture parses without error.
#[test]
fn crl_parse_ecdsa_v2_signature_algorithm() {
    let crl = load_crl("cert/asn1/ecdsa_crl/crl_v2.der");
    assert_eq!(crl.version, 2);
}

/// DSA row — Rust's CRL parser currently rejects DSA-SHA-256 CRLs
/// (`InvalidCrl("unknown TBS signature algorithm: 2.16.840.1.101.3.4.3.2")`).
/// Pin the gap so a future signature-algorithm extension lands as a
/// deliberate change (DSA was deprecated by FIPS 186-5 but legacy
/// fixtures persist).
#[test]
fn crl_parse_dsa_v2_signature_algorithm_unsupported_gap() {
    let path = format!(
        "{}/../../tests/vectors/c-asn1-fixtures/cert/asn1/dsa_crl/crl_v2.der",
        env!("CARGO_MANIFEST_DIR")
    );
    let bytes = std::fs::read(&path).unwrap();
    let err = CertificateRevocationList::from_der(&bytes)
        .expect_err("DSA-SHA-256 CRL must currently fail to parse");
    let msg = format!("{err:?}");
    assert!(
        msg.contains("2.16.840.1.101.3.4.3.2") || msg.to_ascii_lowercase().contains("unknown"),
        "DSA-SHA-256 reject should mention the unsupported OID or 'unknown'; got: {msg}"
    );
    // TODO(#42-phase-c): add DSA-SHA-256 to the CRL signature-algorithm
    // dispatch table if DSA verification ever becomes a target.
}

/// SM2 row — Chinese cryptographic algorithm; mirrors the GM/T 0009
