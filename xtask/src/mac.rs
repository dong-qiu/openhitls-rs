/// CMAC `.data` rows that carry a real KAT (`alg : key : msg : expected`)
/// all use the `_FUN_TC004` suffix. Everything else — context lifecycle,
/// provider stubs, alignment probes — is API-surface that doesn't map to
/// the Rust API.
pub fn emit_cmac_kat(cases: &[TestCase]) -> (String, EmitStats) {
    let mut body = String::new();
    let mut stats = EmitStats::default();
    let mut used_sm4 = false;

    for case in cases {
        match classify_cmac(&case.tc_name) {
            CaseKind::OneShot => emit_cmac_one_shot(&mut body, case, &mut stats, &mut used_sm4),
            CaseKind::ApiSurface => stats.skipped_api += 1,
            CaseKind::Unknown => stats.skipped_unknown += 1,
        }
    }

    let mut out = String::new();
    write_cmac_header(&mut out, used_sm4);
    out.push_str(&body);
    write_footer(&mut out, &stats, cases.len());
    (out, stats)
}

fn classify_cmac(tc_name: &str) -> CaseKind {
    // Context-lifecycle and provider/stub patterns are not portable to Rust.
    if tc_name.contains("_API_TC")
        || tc_name.contains("_COPY_CTX_")
        || tc_name.contains("_DUP_CTX_")
        || tc_name.contains("_DEFAULT_PROVIDER_")
        || tc_name.contains("_STUB_TC")
        || tc_name.contains("_ADDR_NOT_ALIGN_")
    {
        return CaseKind::ApiSurface;
    }
    // FUN_TC004 is the only row shape carrying a 4-arg KAT.
    // FUN_TC001/002/003/005/006 are workflow / repetition-count tests.
    if tc_name.contains("_FUN_TC004") {
        CaseKind::OneShot
    } else if tc_name.contains("_FUN_TC") {
        CaseKind::ApiSurface
    } else {
        CaseKind::Unknown
    }
}

/// Map a CMAC algorithm symbol to (short label, cipher kind).
fn cmac_alg(symbol: &str) -> Option<(&'static str, CmacCipher)> {
    match symbol {
        "CRYPT_MAC_CMAC_AES128" => Some(("aes128", CmacCipher::Aes)),
        "CRYPT_MAC_CMAC_AES192" => Some(("aes192", CmacCipher::Aes)),
        "CRYPT_MAC_CMAC_AES256" => Some(("aes256", CmacCipher::Aes)),
        "CRYPT_MAC_CMAC_SM4" => Some(("sm4", CmacCipher::Sm4)),
        _ => None,
    }
}

#[derive(Clone, Copy)]
enum CmacCipher {
    Aes,
    Sm4,
}

fn write_cmac_header(out: &mut String, used_sm4: bool) {
    out.push_str(
        "// This file is GENERATED by `cargo xtask migrate-c-tests --algo cmac`.\n\
         // DO NOT EDIT BY HAND. Source: openhitls C SDV test_suite_sdv_eal_mac_cmac.data\n\
         //\n\
         // Generator: docs/c-test-migration-plan.md Phase A (xtask).\n\n",
    );
    if used_sm4 {
        out.push_str("#![cfg(all(feature = \"cmac\", feature = \"aes\", feature = \"sm4\"))]\n\n");
        out.push_str("use hitls_crypto::cmac::{Cmac, CmacSm4};\n\n");
    } else {
        out.push_str("#![cfg(all(feature = \"cmac\", feature = \"aes\"))]\n\n");
        out.push_str("use hitls_crypto::cmac::Cmac;\n\n");
    }
}

fn emit_cmac_one_shot(
    out: &mut String,
    case: &TestCase,
    stats: &mut EmitStats,
    used_sm4: &mut bool,
) {
    // Expected: 4 args = alg, key, msg, expected.
    if case.args.len() != 4 {
        stats.skipped_unknown += 1;
        return;
    }
    let Some(alg) = case.args[0].as_symbol() else {
        stats.skipped_unknown += 1;
        return;
    };
    let Some(key) = case.args[1].as_hex() else {
        stats.skipped_unknown += 1;
        return;
    };
    let Some(msg) = case.args[2].as_hex() else {
        stats.skipped_unknown += 1;
        return;
    };
    let Some(expected) = case.args[3].as_hex() else {
        stats.skipped_unknown += 1;
        return;
    };
    let Some((label, cipher)) = cmac_alg(alg) else {
        stats.skipped_unsupported_alg += 1;
        return;
    };
    let cmac_ty = match cipher {
        CmacCipher::Aes => "Cmac",
        CmacCipher::Sm4 => {
            *used_sm4 = true;
            "CmacSm4"
        }
    };

    let fn_name = format!("tc_line{}_cmac_{}_one_shot", case.line, label);
    write_cmac_test_doc(out, case);
    writeln!(out, "#[test]").unwrap();
    writeln!(out, "fn {fn_name}() {{").unwrap();
    writeln!(out, "    let key: &[u8] = {};", format_byte_slice(key)).unwrap();
    writeln!(out, "    let msg: &[u8] = {};", format_byte_slice(msg)).unwrap();
    writeln!(
        out,
        "    let expected: &[u8] = {};",
        format_byte_slice(expected)
    )
    .unwrap();
    writeln!(out, "    let mut cmac = {cmac_ty}::new(key).unwrap();").unwrap();
    writeln!(out, "    cmac.update(msg).unwrap();").unwrap();
    writeln!(out, "    let mut actual = [0u8; 16];").unwrap();
    writeln!(out, "    cmac.finish(&mut actual).unwrap();").unwrap();
    writeln!(out, "    assert_eq!(actual.as_slice(), expected);").unwrap();
    writeln!(out, "}}\n").unwrap();
    stats.emitted += 1;
}

fn write_cmac_test_doc(out: &mut String, case: &TestCase) {
    if let Some(desc) = &case.description {
        writeln!(out, "/// {desc}").unwrap();
    }
    writeln!(
        out,
        "/// C source: {} (line {}, one-shot CMAC KAT)",
        case.tc_name, case.line
    )
    .unwrap();
}
