// ---------------------------------------------------------------------------
// TLS 1.2 PRF — (macAlg, secret, label, seed, expected). Lives in hitls-tls.
// `prf` concatenates label‖seed internally, so we pass label="" and seed =
// (label ++ seed) to reproduce the identical P_hash input without needing
// the label to be valid UTF-8.
//
// HashAlgId variants in `hitls_tls::crypt`: Sha1, Sha256, Sha384, Sha512
// (the SHA-512 variant is migration-test-only — TLS 1.2 itself never
// negotiates a SHA-512 PRF, the entry is added so the C SDV
// `CRYPT_MAC_HMAC_SHA512` PRF vectors can be migrated byte-exactly).
// ---------------------------------------------------------------------------

fn mac_to_hashalgid(symbol: &str) -> Option<&'static str> {
    match symbol {
        "CRYPT_MAC_HMAC_SHA256" => Some("Sha256"),
        "CRYPT_MAC_HMAC_SHA384" => Some("Sha384"),
        "CRYPT_MAC_HMAC_SHA512" => Some("Sha512"),
        "CRYPT_MAC_HMAC_SHA1" => Some("Sha1"),
        _ => None,
    }
}

pub fn emit_kdf_tls12_kat(cases: &[TestCase]) -> (String, EmitStats) {
    let mut body = String::new();
    let mut stats = EmitStats::default();

    for case in cases {
        if is_api_surface(&case.tc_name) || !case.tc_name.contains("_FUN_TC001") {
            stats.skipped_api += 1;
            continue;
        }
        let (Some(alg), Some(secret), Some(label), Some(seed), Some(expected)) = (
            case.args.first().and_then(|a| a.as_symbol()),
            case.args.get(1).and_then(|a| a.as_hex()),
            case.args.get(2).and_then(|a| a.as_hex()),
            case.args.get(3).and_then(|a| a.as_hex()),
            case.args.get(4).and_then(|a| a.as_hex()),
        ) else {
            stats.skipped_unknown += 1;
            continue;
        };
        let Some(variant) = mac_to_hashalgid(alg) else {
            stats.skipped_unsupported_alg += 1;
            continue;
        };
        // P_hash input is label ‖ seed; fold both into a single seed slice.
        let mut label_seed = label.to_vec();
        label_seed.extend_from_slice(seed);
        write_doc(&mut body, case, "TLS 1.2 PRF KAT");
        writeln!(body, "#[test]").unwrap();
        writeln!(
            body,
            "fn tc_line{}_tls12_prf_{}() {{",
            case.line,
            variant.to_lowercase()
        )
        .unwrap();
        writeln!(
            body,
            "    let secret: &[u8] = {};",
            format_byte_slice(secret)
        )
        .unwrap();
        writeln!(
            body,
            "    let label_seed: &[u8] = {};",
            format_byte_slice(&label_seed)
        )
        .unwrap();
        writeln!(
            body,
            "    let expected: &[u8] = {};",
            format_byte_slice(expected)
        )
        .unwrap();
        writeln!(
            body,
            "    let actual = prf(HashAlgId::{variant}, secret, \"\", label_seed, expected.len()).unwrap();"
        )
        .unwrap();
        writeln!(body, "    assert_eq!(actual.as_slice(), expected);").unwrap();
        writeln!(body, "}}\n").unwrap();
        stats.emitted += 1;
    }

    let mut out = String::new();
    out.push_str(
        "// This file is GENERATED by `cargo xtask migrate-c-tests --algo kdf-tls12`.\n\
         // DO NOT EDIT BY HAND. Source: openhitls C SDV test_suite_sdv_eal_kdf_tls12.data\n\
         //\n\
         // Generator: docs/c-test-migration-plan.md Phase A (xtask).\n\n\
         use hitls_tls::crypt::prf::prf;\n\
         use hitls_tls::crypt::HashAlgId;\n\n",
    );
    out.push_str(&body);
    write_footer(&mut out, &stats, cases.len());
    (out, stats)
}
